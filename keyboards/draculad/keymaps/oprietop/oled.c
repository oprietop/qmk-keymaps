oled_rotation_t oled_init_user(oled_rotation_t rotation) {
if (is_keyboard_master()){
  if(is_keyboard_left()){
    return OLED_ROTATION_270;
  } else {
    return OLED_ROTATION_90;
  }
} else {
    return OLED_ROTATION_0;
  }
}

void add_keylog(uint16_t keycode) {
  if ((keycode >= QK_MOD_TAP && keycode <= QK_MOD_TAP_MAX) || (keycode >= QK_LAYER_TAP && keycode <= QK_LAYER_TAP_MAX)) {
    keycode = keycode & 0xFF;
  }
  for (uint8_t i = 4; i > 0; --i) {
    keylog_str[i] = keylog_str[i - 1];
  }
  if (keycode < 60) {
    keylog_str[0] = code_to_name[keycode];
  }
  log_timer = timer_read();
}

void update_log(void) {
  if (timer_elapsed(log_timer) > 750) {
    add_keylog(0);
  }
}

void render_bootmagic_status(void) {
  if (keymap_config.swap_lalt_lgui) {
    oled_write_P(PSTR("<Mac OS>  "), false);
  } else {
    oled_write_P(PSTR("<LINUX>   "), false);
  }
}

void render_default_layer_state(void) {
  oled_write_P(PSTR("BASE:     "), false);
  switch (biton32(default_layer_state)) {
   case _QW:
      oled_write_P(PSTR(" Qwerty   "), false);
      break;
    case _DH:
      oled_write_P(PSTR(" Mod DH   "), false);
      break;
    case _DV:
      oled_write_P(PSTR(" Dvorak   "), false);
      break;
  }
}

void render_layer_state(void) {
  oled_write_P(PSTR("LAYER:    "), false);
  switch (biton32(layer_state)) {
    case 0:
      oled_write_P(PSTR(" Default  "), false);
      break;
    case _RA:
      oled_write_P(PSTR(" Raise    "), false);
      break;
    case _LO:
      oled_write_P(PSTR(" Lower    "), false);
      break;
    case _EX:
      oled_write_P(PSTR(" Extra    "), false);
      break;
    case _AD:
      oled_write_P(PSTR(" Adjust   "), false);
      break;
    case _EM:
      oled_write_P(PSTR(" Emoji    "), false);
      break;
    default:
      oled_write_P(PSTR(" Unknown  "), false);
      break;
  }
}

void render_keylock_status(uint8_t led_usb_state) {
  oled_write_P(PSTR("LOCKS:    "), false);
  oled_write_P(PSTR(" Num lock "), led_usb_state & (1 << USB_LED_NUM_LOCK));
  oled_write_P(PSTR(" Caps lock"), led_usb_state & (1 << USB_LED_CAPS_LOCK));
  oled_write_P(PSTR(" Scr lock "), led_usb_state & (1 << USB_LED_SCROLL_LOCK));
}

void render_mod_status(uint8_t modifiers) {
  oled_write_P(PSTR("MODS:     "), false);
  oled_write_P(PSTR(" Shift    "), (modifiers & MOD_MASK_SHIFT));
  oled_write_P(PSTR(" Control  "), (modifiers & MOD_MASK_CTRL));
  oled_write_P(PSTR(" GUI      "), (modifiers & MOD_MASK_GUI));
  oled_write_P(PSTR(" ALT      "), (modifiers & MOD_MASK_ALT));
}

void render_keylogger_status(void) {
  oled_write_P(PSTR("LOG: "), false);
  oled_write(keylog_str, false);
}

void render_banner(void) {
  oled_write_P(PSTR("<DracuLad>"), false);
}

void render_wpm(void) {
  uint8_t n = get_current_wpm();
  char    wpm_counter[4];
  wpm_counter[3] = '\0';
  wpm_counter[2] = '0' + n % 10;
  wpm_counter[1] = (n /= 10) % 10 ? '0' + (n) % 10 : (n / 10) % 10 ? '0' : ' ';
  wpm_counter[0] = n / 10 ? '0' + n / 10 : ' ';
  oled_write_P(PSTR("WPM: "), false);
  oled_write(wpm_counter, false);
  oled_write_P(PSTR("  "), false);
}

void render_logo(void) {
  oled_write_raw_P(logo, sizeof(logo));
}

void render_status_secondary(void) {
  render_logo();
  oled_scroll_left(); // Turns on scrolling
}
void render_status_main(void) {
  render_banner();
  //render_bootmagic_status();
  render_default_layer_state();
  render_layer_state();
  render_keylock_status(host_keyboard_leds());
  render_mod_status(get_mods());
  render_wpm();
  render_keylogger_status();
}

bool oled_task_user(void) {
  update_log();
  if (is_keyboard_master()) {
    render_status_main();
  } else {
    render_status_secondary();
  }

  return true;
}
